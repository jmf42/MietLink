export function createLanguageSystem() {
  const [language, setLanguageState] = useState<Language>("en");

  useEffect(() => {
    const savedLanguage = localStorage.getItem("mietlink-language") as Language;
    if (savedLanguage && ["de", "fr", "it", "en", "es"].includes(savedLanguage)) {
      setLanguageState(savedLanguage);
    }
  }, []);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("mietlink-language", lang);
  };

  const t = (
    key: string,
    vars: Record<string, string | number> = {}
  ): string => {
    const template = (translations[language] as any)?.[key] || key;
    return Object.keys(vars).reduce(
      (str, varKey) => str.replace(`{${varKey}}`, String(vars[varKey])),
      template
    );
  };

  return {
    language,
    setLanguage,
    t,
  };
}